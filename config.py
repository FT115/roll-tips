"""
滚动提词器配置文件
===============
修改此文件中的参数来控制提词器的外观和行为。
"""

# ==================== 显示设置 ====================

# 是否全屏模式（True=全屏，False=窗口模式）
FULLSCREEN = True

# 窗口模式下的分辨率（仅在 FULLSCREEN=False 时生效）
WINDOW_WIDTH = 1280
WINDOW_HEIGHT = 720

# 背景颜色 (R, G, B)，0-255
BACKGROUND_COLOR = (0, 0, 0)        # 黑色背景

# 文字颜色 (R, G, B)，0-255
TEXT_COLOR = (255, 255, 255)        # 白色文字

# ==================== 字体设置 ====================

# 字体大小（像素）
FONT_SIZE = 70

# 字体文件路径，None 表示使用系统默认字体
# Windows 中文推荐: "C:/Windows/Fonts/msyh.ttc"（微软雅黑）
# 或 "C:/Windows/Fonts/simhei.ttf"（黑体）
FONT_PATH = None

# 行间距（像素），即两行文字之间的额外空白
LINE_SPACING = 20

# 左右边距（像素），文字距离屏幕左右边缘的距离
MARGIN_LEFT = 120
MARGIN_RIGHT = 120

# 文字区域距离屏幕顶部的留白（像素）
MARGIN_TOP = 80
# 文字区域距离屏幕底部的留白（像素）
MARGIN_BOTTOM = 80

# ==================== 滚动设置 ====================

# 滚动速度（像素/秒），数值越大滚动越快
# 建议范围：30 ~ 120
SCROLL_SPEED = 40

# ==================== 时间设置 ====================

# 脚本开始后，文字开始滚动前的等待时间（秒）
DELAY_BEFORE_START = 2.0

# 文字全部滚出屏幕后，停留等待的时间（秒）
WAIT_AFTER_END = 3.0

# ==================== 文字来源 ====================

# 提词文本文件路径
WORDS_FILE = "words.txt"

# 文本文件编码
FILE_ENCODING = "utf-8"
